package Modelo;

/**
 *
 * @author Daniel
 */
public class Idioma {
    
    private int id;
    private String idioma;

    public Idioma() {
    }

    public Idioma(int id, String idioma) {
        this.id = id;
        this.idioma = idioma;
    }
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIdioma() {
        return idioma;
    }

    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }
    
    
    
}
