    private String getFechaHoy() {
        String fecha;      
        Date hoy = new Date();
        String dia = String.valueOf(hoy.getDate());
        String mes = String.valueOf(hoy.getMonth()+1);
        String ano = String.valueOf(hoy.getYear()+1900);

        fecha = ano + "-" + mes + "-" + dia;

        return fecha;
    }

