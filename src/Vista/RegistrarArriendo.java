    private void btnConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConfirmarMouseClicked
        int idCliente = 0;
        for (int i = 0; i < listaClientes.size(); i++) {
            if (listaClientes.get(i).getRut().equals((String) boxCliente.getSelectedItem())) {
                idCliente = listaClientes.get(i).getId();

                if (new ControladorArriendo().crear(new Arriendo(id, getFechaHoy() ,getFechaDevolucion(), Integer.parseInt(txtCostoArriendo.getText()), idCliente, 1))) {
                    int size = lista.getRowCount();
                    for (int j = 0; j < size; j++) {
                        if (new ControladorLibro().agregarIdArriendo((String) lista.getModel().getValueAt(j, 0), id)) {
                            JOptionPane.showMessageDialog(null, "Se ha guardado exitosamente");
                        } else {
                            JOptionPane.showMessageDialog(null, "Ha ocurrido un error al hacer cambio de idarriendo en el libro");
                        }

                    }
                    Principal.principal.Arriendo();
                } else {
                    JOptionPane.showMessageDialog(null, "error al registrar el arriendo");
                }
            }

        }

        
    }//GEN-LAST:event_btnConfirmarMouseClicked


