    private void boxTitulos() {
        ArrayList<TituloLibro> listaTituloDisponible = new ArrayList<>();
        ArrayList<TituloLibro> listaTitulo = new ControladorTitulo().listado();
        for (int i = 0; i < listaTitulo.size(); i++) {
            String titulo = listaTitulo.get(i).getTitulo();
            for (int j = 0; j < listaArrendar.size(); j++) {
                if (titulo.equals(listaArrendar.get(j).getTitulo())) {
                    boolean existe = false;
                    for (int x = 0; x < listaTituloDisponible.size(); x++) {
                        if (titulo.equals(listaTituloDisponible.get(x).getTitulo())) {
                            existe = true;
                        }
                    }
                    if (!existe) {
                        listaTituloDisponible.add(listaTitulo.get(i));
                    }
                }
            }
        }
        for (int i = 0; i < listaTituloDisponible.size(); i++) {
            boxTitulo.addItem(listaTituloDisponible.get(i).getTitulo());
        }
    }

