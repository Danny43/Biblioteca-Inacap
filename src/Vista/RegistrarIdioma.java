    public void llenarTablita() {
        ControladorIdioma cId = new ControladorIdioma();
        ArrayList<Idioma> listaId = cId.listado();

        Object model[][] = new Object[listaId.size()][1];

        for (int i = 0; i < listaId.size(); i++) {
            model[i][0] = listaId.get(i).getIdioma();
        }

        tablaIdioma.setModel(new DefaultTableModel(model, new String[]{"Idioma"}));
    }

