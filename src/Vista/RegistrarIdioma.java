    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        if (txtidioma.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe poner datos en el campo de Idioma");
        } else {
            if (new ControladorIdioma().crear(new Idioma(txtidioma.getText()))) {
                JOptionPane.showMessageDialog(null, "Se ha agregado correctamente");
                txtidioma.setText("");
                llenarTablita();
            } else {
                JOptionPane.showMessageDialog(null, "Ha ocurrido un error");
            }
        }


    }//GEN-LAST:event_btnAgregarMouseClicked

